			工具栏右边提供浏览模式切换和搜索功能（需要启用搜索）。<img alt="工具栏" src="images/help_toolbar_right.png"/>
		</p>
		<p>
			在搜索框中输入需要搜索的关键字，点击搜索图标<img alt="搜索" src="images/toolbar-search.png"/>后，将在当前目录及其子目录中搜索符合关键字的目录名和文件名，并显示出来。
