		<h4>设置</h4>
		<p>
			<a class="lightboxImg" title="help_setting_large.png" href="images/help_setting_large.png">
				<img class="helpLeftImg" alt="设置" src="images/help_setting_small.png" />
			</a>
			在设置页面，您可以修改安装时为 Rosefinch（朱雀）设置的各个参数。设置内容包括基本设置、时区和语言、搜索、数据库设置和其他设置。
		</p>
		<p>
			基本设置。您可以修改根目录路径类型：“绝对地址”表示您输入的是绝对地址，“相对地址”表示您输入的地址是相对于 Rosefinch（朱雀）目录的。 您还可以修改路径：存有您希望在 Rosefinch（朱雀）中显示的文件的目录地址。 
		</p>
		<p>
			时区和语言设置。你可以修改字符集： 运行 Rosefinch（朱雀）的操作系统在文件系统中所使用的字符集。Windows 使用本地字符集，例如 GB2312；*nix 多使用 UTF-8。 你可以修改时区：运行 Rosefinch（朱雀）的服务器所使用的时区。 您还可以修改语言：Rosefinch（朱雀）所使用的语言。 
		</p>
		<p>
			搜索和数据库设置。您可以选择是否启用搜索功能：如果启用了搜索功能，需要设置数据库参数。在数据库设置页面中，您需要设置数据库用户名、数据库用户密码、数据库名称和数据库主机，完成后会有提示是否设置正确。索引所有文件按钮可以指示 Rosefinch（朱雀）索引目录下的所有子目录和文件信息，以便进行搜索。当文件信息发生变化时，Rosefinch（朱雀）会自动更新信息，你并不需要经常索引所有文件。索引完成后会有提示。
		</p>
		<p>
			其他设置。您可以修改标题：它是 Rosefinch（朱雀）主界面中显示的标题。您可以选择是否为图片启用 LightBox：如果启用，在主界面的文件列表中单击图片文件将显示图片预览。您还可以选择为音乐文件启用 Audio Player：如果启用，在主界面的文件列表中单击 mp3 音频文件将显示音频预览。
		</p>
