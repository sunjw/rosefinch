        <h4>Rosefinch is a web file manager written in php.</h4>
        <p>It provides features that you can manage files on local or remote computer. You can cut, copy, paste, rename and delete files or directories, and even make a new directory and upload a file (need higher user right). Of course, you can downlaod file from Rosefinch. Rosefich can provide search feature if there is a database server.</p>
        <h4>Rosefinch is platform independence and has multi-language feature.</h4>
        <p>You can run Rosefinch on Apache (recommended) or IIS (lost some functions) and other php execute. You can use Linux, Windows or other OS.</p>
        <p>You just need do some simple settings, then Rosefinch can run on you system.</p>
        <p>Rosefinch provides multi-language for you. Also, you can add your local language.</p>
        <p>Rosefinch provides preview for images and mp3 files.</p>
        <h4>Web browser require</h4>
        <p>Because of using javascript and ajax, you should allow browser to support javascript. Rosefinch uses Cookie to store user settings, you should allow browser to support Cookie. If you want to use a full function version of Rosefinch, please use IE 7 or newer version of IE browser, or use modern browsers like <strong>Firefox</strong>.</p>
